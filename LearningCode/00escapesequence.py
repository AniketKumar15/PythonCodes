# Escape Sequence
# Escape sequences are special characters that are used to represent certain characters in strings.
# They are used to represent characters that are not easily typed on the keyboard or to represent special characters that have a specific meaning in Python.
# # Escape sequences start with a backslash (\) and are followed by one or more characters.
# # Some common escape sequences are:
# # \n - New line
# # \t - Tab
# # \r - Carriage return
# # \b - Backspace
# # \f - Form feed
# # \a - Bell
# # \v - Vertical tab
# # \\\ - Backslash
# # \' - Single quote
# # \" - Double quote
# # \ooo - Octal value
# # \xhh - Hexadecimal value

print("Hello\nWorld")
print("Hello\tWorld")
print("Hello\rWorld")
print("Hello\bWorld")
print("Hello\fWorld")
print("Hello\aWorld")
print("Hello\vWorld")
print("Hello\\World")
print("Hello\'World\'")
print("Hello\"World\"")

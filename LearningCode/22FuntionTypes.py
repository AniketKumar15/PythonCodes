# Types of Functions in Python -
# 1. Built-in Functions -> These are functions that are already defined in Python and can be used directly without any additional code. Examples include `print()`, `len()`, `type()`, etc.
# 2. User-defined Functions -> These are functions that you define yourself to perform specific tasks. You can create them using the `def` keyword, followed by the function name and parameters.
# 3. Lambda Functions -> These are small anonymous functions defined using the `lambda` keyword. They can take any number of arguments but can only have one expression. Lambda functions are often used for short, throwaway functions that are not reused elsewhere in the code.


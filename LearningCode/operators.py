# Operator in Python
# Operators are special symbols that perform operations on variables and values. Python has several types of operators, including:
# 1. Arithmetic Operators: Used to perform basic mathematical operations.(+,-,*,/,%,//,**)
# 2. Comparison Operators: Used to compare two values and return a boolean result.(==,!=,>,<,>=,<=)
# 3. Assignment Operators: Used to assign values to variables.(=,+=,-=,*=,/=,%=,//=,**=)
# 4. Logical Operators: Used to combine conditional statements.(and,or,not)
# 5. Bitwise Operators: Used to perform bit-level operations.(&,|,^,~,<<,>>)
# 6. Identity Operators: Used to check if two variables point to the same object.(is,is not)
# 7. Membership Operators: Used to check if a value is present in a sequence.(in,not in)
# 8. Ternary Operator: A shorthand way to write an if-else statement.
# 9. Lambda Operator: Used to create anonymous functions.


# Arithmetic Operators
print("Arithmetic Operators")
a=7
b=3
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Floor Division:", a // b)
print("Exponentiation:", a ** b)
print("\n")
# Comparison Operators
print("Comparison Operators")
print("Equal to:", a == b)
print("Not Equal to:", a != b)
print("Greater than:", a > b)
print("Less than:", a < b)
print("Greater than equal to:", a >= b)
print("Less than equal to:", a <= b)


# Fun fact: the + operator can also be used to concatenate strings and lists in python.